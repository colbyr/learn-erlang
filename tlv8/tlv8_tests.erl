-module(tlv8_tests).
-include_lib("eunit/include/eunit.hrl").

decode_test() ->
  #{} = tlv8:decode(<<>>),

  #{ 16#ff := <<16#01, 16#02, 16#03>> } = tlv8:decode(<<16#ff, 16#03, 16#01, 16#02, 16#03>>),

  #{
      16#ff := <<16#01, 16#02, 16#03>>,
      16#00 := <<16#01, 16#02>>
   } = tlv8:decode(
    <<
      16#ff, 16#03, 16#01, 16#02, 16#03,
      16#00, 16#02, 16#01, 16#02
    >>
  ).

